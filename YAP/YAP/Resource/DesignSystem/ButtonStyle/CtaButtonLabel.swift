/// CTA 버튼에 사용되는 버튼 이름 케이스
///
/// - Cases:
///   - next: "다음"
///   - calculate: "계산하기"
///   - start: "시작하기"
///   - add: "추가하기"
///   - eat: "이대로 먹기"
public enum CtaButtonLabel: String {
  case next = "다음"
  case calculate = "계산하기"
  case start = "시작하기"
  case add = "추가하기"
  case eat = "이대로 먹기"
}
