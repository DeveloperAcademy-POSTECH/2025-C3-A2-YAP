/// CTA 버튼에 사용되는 버튼 이름 케이스
///
/// - Cases:
///   - next: "다음"
///   - before: "이전"
///   - calculate: "계산하기"
///   - start: "시작하기"
///   - add: "음식 추가"
///   - eat: "식단 추가"
///   - burn: "운동하기"
///   - controlMeal: "식단조절"
public enum CtaButtonLabel: String {
  case next = "다음"
  case before = "이전"
  case calculate = "계산하기"
  case start = "시작하기"
  case add = "음식 추가"
  case eat = "식단 추가"
  case burn = "운동하기"
  case controlMeal = "식단조절"
}
