/// CTA 버튼에 사용되는 컬러 케이스
///
/// - Cases:
///   - main: 메인 색상
///   - primaryActive: 메인 액티브
///   - primaryHover: 메인 호버
///   - light: 라이트 색상
///   - lightActive: 라이트 액티브
///   - lightHover: 라이트 호버
///   - white: 흰색
public enum CtaButtonColor: String {
  case main
  case primaryActive
  case primaryHover
  case light
  case lightActive
  case lightHover
  case white
}
